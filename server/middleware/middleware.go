// Cadena de conexión de base de datos
// const connectionString = "mongodb://localhost:27017"
const connectionString = "mongodb://localhost:27017/go-react-todo_mongodb_1" // docker

// Nombre de la base de datos, crear desde shell o robo3t
const dbName = "todos"

// Nombre de la colección
const collName = "todolist"

// collection object/instance
var collection *mongo.Collection

// crear conexión con mongodb
func init() {

	// Establecer opciones de cliente
	clientOptions := options.Client().ApplyURI(connectionString)

	// conectarse a MongoDB
	client, err := mongo.Connect(context.TODO(), clientOptions)

	if err != nil {
		log.Fatal(err)
	}

	// Comprobar conexión
	err = client.Ping(context.TODO(), nil)

	if err != nil {
		log.Fatal(err)
	}

	fmt.Println("Connected to MongoDB!")

	collection = client.Database(dbName).Collection(collName)

	fmt.Println("Collection instance created!")
}

// GetAllTask route
func GetAllTask(w http.ResponseWriter, r *http.Request) {
	w.Header().Set("Context-Type", "application/x-www-form-urlencoded")
	w.Header().Set("Access-Control-Allow-Origin", "*")
	payload := getAllTask()
	json.NewEncoder(w).Encode(payload)
}

